package pe.unsa.epc;

public class Person {
		private String name;
		private int edad;
		public Person() 
		{
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public int getEdad() {
			return edad;
		}
		public void setEdad(int edad) {
			this.edad = edad;
		}
	

}
